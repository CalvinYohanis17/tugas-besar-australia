function tampilPesan(era) {
    
    if (era === 'aborigin') {
        alert("Suku Aborigin menggunakan konsep 'Dreamtime' (Waktu Impian) untuk menceritakan bagaimana roh leluhur menciptakan alam semesta, sungai, dan gunung di Australia.");
    } else if (era === 'modern') {
        alert("Garis Waktu Singkat:\n1788: Kedatangan narapidana pertama Inggris.\n1851: Demam Emas (Gold Rush) menarik ribuan imigran dunia.\n1901: Australia resmi menjadi satu federasi negara.");
    } else {
        alert("Sejarah menarik lainnya akan segera hadir!");
    }
}